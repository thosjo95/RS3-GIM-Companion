/**
 * Determine the auto stage for a group based on average total level.
 * totalLevels: array of each player's Overall skill level (sum of all skills).
 */
export function detectStage(players) {
  if (!players?.length) return 'early';
  const avgTotal = players.reduce((sum, p) => sum + (p.total_level || 0), 0) / players.length;
  if (avgTotal >= STAGES.end.minTotal)  return 'end';
  if (avgTotal >= STAGES.mid.minTotal)  return 'mid';
  return 'early';
}

/**
 * Check if a player meets all skill requirements for a suggestion.
 * Returns { ready: bool, failed: [{ skill, have, need }] }
 */
export function checkPlayerReadiness(player, suggestion) {
  const failed = [];
  for (const [skill, needed] of Object.entries(suggestion.requirements?.skills ?? {})) {
    const have = player.skills?.find(s => s.skill_name === skill)?.level ?? 1;
    if (have < needed) failed.push({ skill, have, need: needed });
  }
  return { ready: failed.length === 0, failed };
}
